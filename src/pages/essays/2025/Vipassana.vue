        </li>
        <li>
          內觀中心以佈施（dāna）維繫，你必須至少完成一次的 10 日課程，才可以捐款。工作人員都是志工。這個結構也是提醒學員：把注意力放在練習與改變上，而非交易（你花了多少錢）與期待（我花了錢怎麼還吃
          X X X 住在 X X X ）。
        </li>
      </ul>
